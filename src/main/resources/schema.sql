-- Schema for PRICES table (PostgreSQL syntax)
-- This table stores pricing information for products across different brands and time periods

DROP TABLE IF EXISTS PRICES;

CREATE TABLE PRICES (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BRAND_ID INT NOT NULL,
    START_DATE TIMESTAMP NOT NULL,
    END_DATE TIMESTAMP NOT NULL,
    PRICE_LIST INT NOT NULL,
    PRODUCT_ID BIGINT NOT NULL,
    PRIORITY INT NOT NULL,
    PRICE DECIMAL(10, 2) NOT NULL,
    CURR VARCHAR(3) NOT NULL
);

-- Create indexes for better query performance
CREATE INDEX IDX_PRICES_QUERY ON PRICES(PRODUCT_ID, BRAND_ID, START_DATE, END_DATE);
